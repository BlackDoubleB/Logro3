function suma(a,b){
    return(a+b);
}

function resta(a,b){
    return(a-b);
}

function multiplicacion(a,b){
    return(a*b);
}

function division(a,b){
    return(a/b);
}

function potenciacion(a, b) {
    return a ** b;
}


